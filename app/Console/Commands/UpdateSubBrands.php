    public function handle()
    {
        $products = BaseProduct::query()->whereIn('sku', array_keys($this->subBrands))->get();
        $subBrandField = MasterDataField::query()
            ->where('title', 'Sub Brand')
            ->first();

        $subBrands = MasterDataValue::query()
            ->where('cat_id', $subBrandField->cat_id)
            ->get()
            ->pluck('value_id', 'name');

        foreach ($products as $product) {
            $product->sub_brand_id = $subBrands[$this->subBrands[$product->sku]];
            $product->save();
        }
    }
